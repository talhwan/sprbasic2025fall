package com.thc.sprbasic2025fall.dto;

public class MultipleBasket {
    int a;
    int b;

    public MultipleBasket() {}
    public MultipleBasket(int a, int b) {
        this.a = a;
        this.b = b;
    }
    public int getA() {
        return a;
    }
    public void setA(int a) {
        this.a = a;
    }
    public int getB() {
        return b;
    }
    public void setB(int b) {
        this.b = b;
    }
}
